<h2>Sistema Interativo Online de Agendamento de Consultas</h2>

<hr>

<p> 
O sistema tem por objetivo gerenciar as funcionalidades (cadastro, consulta, atualização e remoção de pacientes, profissionais da saúde agendamentos de consultas e interação consulta X paciente) em um consultório (clínicas médicas, odontológicas e outras semelhantes) trazendo agilidade e mobilidade em todos esses processos. Além dessas funções descritas, o sistema traz uma tela de interação consulta X paciente.
O sistema pode ser utilizado em qualquer dispositivo tecnológico com acesso a internet.
</p>

<br>
<p>
O acesso é mediante login e senha disponibilizado pelo administrador do sistema, e após o acesso, o usuário será direcionado a página principal que contém: menu (topo da página) e botões interativos.
</p>

<br>
<p>
No menu principal (topo da página) contém os botões: pacientes, profissionais da saúde, agendamentos, sobre e logout.
</p>

<br>
<p>
Ao clicar sobre os botões o usuário poderá cadastrar, consultar, atualizar e remover os pacientes e profissionais da saúde. No caso dos agendamentos, as funções são as mesmas, porém não será excluída a consulta mas desmarcada. 
</p>

<br>

<p>
O grande diferencial do sistema é a inclusão da consulta interativa que apresenta na tela as consultas do dia atual, que será utilizada pelo profissional da saúde e secretária. O profissional da saúde irá clicar em "Liberar Atendimento" e a secretária irá clicar em "Anunciar Paciente" e uma nova aba do navegador irá mostrar a chamada da consulta.
</p>
<p>
<b>Obs.*Para iniciar sempre o primeiro atendimento a secretária deverá clicar no botão "Liberar Telão", que irá abrir uma nova aba no navegador e então deverá arrastá-la até a tela ampla, a qual mostrará todas as chamadas das consultas
</b>
</p>

